#include "../inc/fdf.h"

void	swap_co(int *x0, int *x1, int *y0, int *y1)
{
	int	x;
	int	y;

	x = *x0;
	*x0 = *x1;
	*x1 = x;
	y = *y0;
	*y0 = *y1;
	*y1 = y;
}
